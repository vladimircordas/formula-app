export function nationToNationality(nation) {
  switch (nation) {
    case 'German':
      return 'Germany';

    case 'Austrian':
      return 'Austria';

    case 'Mercedes':
      return '../images/teams/mercedes.jpg';

    case 'Italian':
      return 'Italy';

    case 'British':
      return 'Great Britain';

    case 'Indian':
      return 'India';

    case 'Swiss':
      return 'Switzerland';

    case 'Russian':
      return 'Russia';

    case 'Malaysian':
      return 'Malaysia';

    default:
      return 'blue';
  }
}